<template>
    <div class='page-article' v-if="model">
      <div class="d-flex py-3 px-2 border-bottom">
        <div class="iconfont icon-Back text-blue"></div>
        <strong class="flex-1 text-blue pl-2">
          {{model.title}}
        </strong>
        <div class="text-grey fs-xs">
          2019-05-12
        </div>
      </div>
      <div v-html="model.content" class="px-3 body fs-lg"></div>
      <div class="px-3 border-top py-2">
        <div class="d-flex ai-center">
          <i class="iconfont icon-menu1"></i>
          <strong class="text-blue fs-lg ml-2">相关资讯</strong>
        </div>
        <div class="pt-1">
          <router-link class="py-2" :to="`/articles/${item._id}`" tag='div' v-for="(item,i) in model.related" :key="i">{{item.title}}</router-link>
        </div>
      </div>
    </div>
</template>

<script>
export default {
    props: {
      id:{type:String,require:true}
    },
    data() {
        return {
          model:null
        };
    },
    watch: {
      // id:'fetch',// 这是下面的简写
      id(){
        this.fetch()
      }
    },
    created() {
      this.fetch()
    },
    methods: {
      async fetch(){
        const res = await this.$http.get(`/articles/${this.id}`)
        this.model = res.data
      }
    }
};
</script>

